#include "libft.h"

int		ft_strcchr(const char *str, char c)
{
	int i;

	if (!str || !c)
		return (0);
	i = 0;
	while (str[i])
	{
		if (str[i] == c)
			return (i);
		i++;
	}
	return (0);
}
