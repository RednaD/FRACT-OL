#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*str1;
	unsigned char	*str2;
	int				i;

	i = 0;
	str1 = (unsigned char*)s1;
	str2 = (unsigned char*)s2;
	while (n)
	{
		if (str1[i] != str2[i])
			return ((unsigned int)(str1[i] - str2[i]));
		i++;
		n--;
	}
	return (0);
}
