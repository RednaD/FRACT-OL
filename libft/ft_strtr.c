#include "libft.h"

void	ft_strtr(char *src, char *dest, int n)
{
	int i;

	if (!src || !dest)
		return ;
	i = 0;
	while (src[i + n])
	{
		dest[i] = src[i + n];
		src[++i + n] = '\0';
	}
}
